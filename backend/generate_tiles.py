def create_emoji_tile(emoji_char, color, size=TILE_SIZE):
    """Create a single emoji tile as a colored image with the emoji rendered on top."""
    img = Image.new("RGBA", (size, size), (0, 0, 0, 0))
    draw = ImageDraw.Draw(img)

    # Try to render the actual emoji using system fonts
    font = None
    font_paths = [
        # Bundle font for Linux servers
        os.path.join(os.path.dirname(__file__), "NotoColorEmoji.ttf"),
        # Windows emoji fonts
        "C:/Windows/Fonts/seguiemj.ttf",
        "C:/Windows/Fonts/segoeui.ttf",
        # Linux system
        "/usr/share/fonts/truetype/noto/NotoColorEmoji.ttf",
        "/usr/share/fonts/noto-cjk/NotoSansCJK-Regular.ttc",
        # macOS
        "/System/Library/Fonts/Apple Color Emoji.ttc",
    ]

    emoji_rendered = False
    for fp in font_paths:
        if os.path.exists(fp):
            try:
                font = ImageFont.truetype(fp, int(size * 0.85))
                # Try to render the emoji
                bbox = draw.textbbox((0, 0), emoji_char, font=font)
                tw = bbox[2] - bbox[0]
                th = bbox[3] - bbox[1]
                if tw > 2 and th > 2:  # Font actually rendered something
                    x = (size - tw) // 2 - bbox[0]
                    y = (size - th) // 2 - bbox[1]
                    draw.text((x, y), emoji_char, font=font, embedded_color=True)
                    emoji_rendered = True
                    break
            except Exception:
                continue

    if not emoji_rendered:
        # Fallback: create a stylized colored tile
        r, g, b = color

        # Draw rounded rectangle background
        margin = 2
        draw.rounded_rectangle(
            [margin, margin, size - margin, size - margin],
            radius=size // 6,
            fill=(r, g, b, 255)
        )

        # Add a subtle highlight/gradient effect
        highlight = Image.new("RGBA", (size, size), (0, 0, 0, 0))
        hdraw = ImageDraw.Draw(highlight)
        for i in range(size // 3):
            alpha = int(60 * (1 - i / (size // 3)))
            hdraw.rectangle(
                [margin + 2, margin + 2 + i, size - margin - 2, margin + 3 + i],
                fill=(255, 255, 255, alpha)
            )
        img = Image.alpha_composite(img, highlight)

        # Add a subtle shadow at the bottom
        shadow = Image.new("RGBA", (size, size), (0, 0, 0, 0))
        sdraw = ImageDraw.Draw(shadow)
        for i in range(size // 6):
            alpha = int(40 * (1 - i / (size // 6)))
            y = size - margin - 1 - i
            sdraw.rectangle(
                [margin + 2, y, size - margin - 2, y + 1],
                fill=(0, 0, 0, alpha)
            )
        img = Image.alpha_composite(img, shadow)

    return img


def generate_all_tiles():
    """Generate all emoji tiles and save them as PNG files."""
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # Create a metadata file for theme categorization
    theme_map = {}
    all_emojis = set()
    total = 0

    for category, emojis in EMOJI_CATEGORIES.items():
        total += len(emojis)

    print(f"Generating {total} emoji tiles...")
    count = 0

    for category, emojis in EMOJI_CATEGORIES.items():
        for emoji_char in emojis:
            if emoji_char in all_emojis:
                continue
            all_emojis.add(emoji_char)

            color = get_emoji_color(emoji_char)
            tile = create_emoji_tile(emoji_char, color)

            # Create a safe filename using the emoji's unicode codepoints
            codepoints = "-".join(f"{ord(c):04x}" for c in emoji_char if ord(c) > 0x20)
            filename = f"emoji_{codepoints}.png"
            filepath = os.path.join(OUTPUT_DIR, filename)

            tile.save(filepath, "PNG")

            # Track category membership
            if filename not in theme_map:
                theme_map[filename] = []
            theme_map[filename].append(category)

            count += 1
            if count % 50 == 0:
                print(f"  Generated {count}/{total} tiles...")

    # Save theme mapping
    theme_path = os.path.join(OUTPUT_DIR, "theme_map.json")
    with open(theme_path, "w", encoding="utf-8") as f:
        json.dump(theme_map, f, indent=2)

    print(f"\nDone! Generated {count} emoji tiles in {OUTPUT_DIR}")
    print(f"Theme mapping saved to {theme_path}")
    return count


if __name__ == "__main__":
    generate_all_tiles()
